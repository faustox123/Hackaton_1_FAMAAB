    public void LlenarTabla(){        //LLenar tabla
        DefaultTableModel tblModel = (DefaultTableModel)TablaOficinas.getModel();    
        for(OfficePoint office:ListaOficinas){
            tblModel.addRow(office.toRow());
        }        
    }  
    
    public void SetListaOficinas(){ //Obtiene la lista de los puntos que son oficinas        
        for(Vertex<RoadPoint> rp :MapGraph.getRoadPoints()){
            RoadPoint p = rp.getElement();
            if(p instanceof OfficePoint){
                ListaOficinas.add((OfficePoint) p);
            }
        }   
    }
    
    public OfficePoint getOfficeUsingName(String OficinaBuscada){ //Obtiene Objeto de la lista usando nombre
        for(OfficePoint ofce:ListaOficinas){
            if(ofce.getNombre().equals(OficinaBuscada))
                return ofce;
        }
        return null;
    }
    
